#include "libft.h"

bool	ft_skip_signs(const char **p)
{
	bool	flag;

	flag = false;
	if (**p == '-' || **p == '+')
	{
		if (**p == '-')
			flag = true;
		(*p)++;
	}
	return (flag);
}
